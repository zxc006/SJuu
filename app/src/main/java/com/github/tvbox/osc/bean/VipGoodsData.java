package com.github.tvbox.osc.bean;

public class VipGoodsData {
    private int price;
    private String des;
    private String desS;
    private String desSS;

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }

    public String getDesS() {
        return desS;
    }

    public void setDesS(String desS) {
        this.desS = desS;
    }

    public String getDesSS() {
        return desSS;
    }

    public void setDesSS(String desSS) {
        this.desSS = desSS;
    }
}
